<?php

namespace App\Http\Controllers;

use App\Book;
use App\Http\Requests\BookStoreRequest;
use Illuminate\Http\Request;
use App\Category;

class BookController extends Controller
{

    /** Set permission methods */
    public function __construct()
    {
        $this->middleware('auth');
        $this->middleware('permission:create category',['only'=>['create','store']]);
        $this->middleware('permission:edit category',['only'=>['edit', 'update']]);
        $this->middleware('permission:delete category',['only'=>['delete','destroy']]);
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $books = Book::all();
        return view('admin.books.index',compact('books'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $categories = Category::all();
        return view('admin.books.create',compact('categories'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(BookStoreRequest $request)
    {
        $book = new Book();
        $book->title = $request->title;
        $book->description = $request->description;
        $book->isbn = $request->isbn;
        $book->category_id = $request->category_id;
        $book->save();

        return redirect()->route('books.index')->with('message','Boek toegevoegd');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Book  $book
     * @return \Illuminate\Http\Response
     */
    public function show(Book $book)
    {
        return view('admin.books.show', compact('book'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Book  $book
     * @return \Illuminate\Http\Response
     */
    public function edit(Book $book)
    {
        //
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Book  $book
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Book $book)
    {
        //
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Book  $book
     * @return \Illuminate\Http\Response
     */

    public function delete(Book $book)
    {
        return view('admin.books.delete',compact('book'));
    }

    public function destroy(Book $book)
    {
        $book->delete();
        return redirect()->route('books.index')->with('message','Boek verwijderd');
    }
}
